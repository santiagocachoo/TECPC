base = float(input('Inserte base del triangulo: '))
altura = float(input('inserte altura del triangulo: '))

area = base * altura / 2
perimetro = base * 3

print(f'El area del triangulo es: {area} cm cuadrados, y el perimetro es: {perimetro} cm.')


