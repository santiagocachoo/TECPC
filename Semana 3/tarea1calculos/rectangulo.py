base = float(input('Inserte base del rectangulo: '))
altura = float(input('Inserte altura del rectangulo: '))

area = base * altura
perimetro = (base*2) + (altura*2)

print(f'El area del rectangulo es: {area} cm cuadrados, y el perimetro es: {perimetro} cm.')

